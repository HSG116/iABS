// Helper to get questions by category with exclusion support
export const getQuestions = (
  category: QuestionCategory | 'ALL', 
  count: number = 10, 
  excludeIds: number[] = []
): Question[] => {
  // 1. Filter by Category
  let filtered = category === 'ALL' 
    ? QUESTIONS_DB 
    : QUESTIONS_DB.filter(q => q.category === category);
  
  // 2. Filter out already used IDs
  if (excludeIds.length > 0) {
    const originalCount = filtered.length;
    filtered = filtered.filter(q => !excludeIds.includes(q.id));
    
    // Fallback: If we run out of questions, use whatever is left
    if (filtered.length < count && originalCount > 0) {
      console.warn("Trivia: Running low on unique questions for this category.");
    }
  }

  // 3. Shuffle
  filtered = filtered.sort(() => 0.5 - Math.random());
  
  // 4. Slice
  return filtered.slice(0, count);
};
